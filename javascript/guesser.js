let points = 0;
let round = 1;
let gameRunning = false;
let tasksCompleted = []

const floorsOfMaps = {
  Oregon: ["second_floor", "first_floor", "basement"],
  Border: ["first_floor", "second_floor"],
  Kafe: ["first_floor", "second_floor", "third_floor"],
  Chalet: ["second_floor", "first_floor", "basement"],
};

const levels = { Oregon, Border, Kafe, Chalet };

function startGame(chosenMap) {
  removeMenu();
  resetGame();
  tasksCompleted = []
  if (!gameRunning) {
    gameRunning = true;
    setupRound(chosenMap);
    highlightMap();
  }
}

function resetGame() {
  points = 0;
  round = 0;
}

function removeElement(id) {
  const element = document.getElementById(id);
  if (element) element.remove();
}

function removeMenu() {
  document.getElementById("menu").remove();
}

function nextRound(chosenMap) {
  if (round < 9) {
    if (gameRunning) {
      round++;
      setupRound(chosenMap);
    }
  } else {
    showEndScreen();
    gameRunning = false;
  }
}

function showEndScreen() {
  const imageContainer = document.getElementById("image-container");
  const endScreen = createElement("div", { id: "menu" });
  const result = createElement("h2", {
    id: "result",
    innerHTML: `Az eredményed:<br> ${points}/10`,
  });
  const button = createElement("button", {
    id: "button",
    innerText: "Vissza a menübe",
    onclick: goToMenu,
  });

  endScreen.append(result, button);
  imageContainer.appendChild(endScreen);
}

function setupRound(chosenMap) {
  const imageContainer = document.getElementById("image-container");
  const randomFloor = getRandomFloor(chosenMap);
  const theRoomToFind = getRoomToFind(randomFloor, chosenMap);
  const title = createElement("h2", {
    id: "title",
    innerHTML: `Találd meg ezt a szobát:<br> ${theRoomToFind}`,
  });
  const pointsAndRounds = createElement("div", {
    id: "points",
    innerHTML: `<p>Kör: ${round+1}/10</p>`,
  });
  const img = createImage(randomFloor, chosenMap);

  imageContainer.append(pointsAndRounds, img, title, );
  setupImageMap(randomFloor, theRoomToFind, chosenMap);
}

function getRandomFloor(map) {
  const floors = floorsOfMaps[map];
  return floors[Math.floor(Math.random() * floors.length)];
}

function getRoomToFind(floor, chosenMap) {
  const levels = {
    Oregon: Oregon.levels,
    Border: Border.levels,
    Kafe: Kafe.levels,
    Chalet: Chalet.levels,
  };

  const rooms = Object.values(levels[chosenMap][getFloorIndex(floor)].rooms);
  
  let randomFloor = rooms[Math.floor(Math.random() * rooms.length)].roomName;
  
  do {
    randomFloor = rooms[Math.floor(Math.random() * rooms.length)].roomName;
  }
  while (tasksCompleted.includes(randomFloor));
  tasksCompleted.push(randomFloor)
  return randomFloor
}

function getFloorIndex(floor) {
  return ["basement", "first_floor", "second_floor", "third_floor"].indexOf(floor);
}

function createImage(floor, chosenMap) {
  const img = createElement("img", {
    id: "img",
    src: `../Images/${chosenMap}_blueprints/r6-maps-${chosenMap.toLowerCase()}-blueprint-${getFloorIndex(floor)}.jpg`,
    useMap: "#image-map",
    className: "map"
  });
  return img;
}

function setupImageMap(floor, targetRoom, chosenMap) {
  const map = createElement("map", { name: "image-map", id: "map" });
  document.getElementById("image-container").appendChild(map);
  const levels = {
    Oregon: Oregon.levels,
    Border: Border.levels,
    Kafe: Kafe.levels,
    Chalet: Chalet.levels,
  };

  Object.values(levels[chosenMap][getFloorIndex(floor)].rooms).forEach(
    (roomData) => {
      const area = createElement("area", {
        shape: "poly",
        coords: roomData.coordinates,
        id: roomData.roomName,
        onclick: () => {
          handleRoomClick(roomData.roomName, targetRoom, chosenMap);
          highlightMap();
        },
      });

      map.appendChild(area);
    }
  );
}

function handleRoomClick(clickedRoom, targetRoom, chosenMap) {
  if (clickedRoom === targetRoom) points++;
  cleanUpRound();
  nextRound(chosenMap);
}

function cleanUpRound() {
  ["map", "title", "img", "points"].forEach((id) => document.getElementById(id).remove());
  document.querySelectorAll(".map").forEach((element) => element.remove());
}

function goToMenu() {
  removeElement("menu");
  const menu = createElement("div", { id: "menu" });
  
  menu.append(
    createElement("h1", { innerText: "Menü" }),
    createElement("p", { innerText: "Ez a játék igénybe fogja venni a tájékozódási képességedet. 4 féle pálya közül választhatsz:" }),
    createMenuButtons()
  );
  
  document.getElementById("content").appendChild(menu);
}

function createMenuButtons() {
  const buttonContainer = createElement("div", { id: "button-grid" });
  ["Border", "Oregon", "Kafe", "Chalet"].forEach(map => {
    const button = createElement("button", {
      innerText: map,
      className: "button",
      onclick: () => startGame(map)
    });
    buttonContainer.appendChild(button);
  });
  return buttonContainer;
}



function createElement(tag, properties = {}) {
  const element = document.createElement(tag);
  Object.assign(element, properties);
  return element;
}

